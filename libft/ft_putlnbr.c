#include "libft.h"

void	ft_putlnbr(long n)
{
	if (n == -9223372036854775807)
		ft_putstr("-9223372036854775808");
	else
	{
		if (n < 0)
		{
			ft_putchar('-');
			n *= -1;
		}
		if (n >= 10)
			ft_putlnbr(n / 10);
		ft_putchar((n % 10) + '0');
	}
}
